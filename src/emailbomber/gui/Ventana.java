    private void iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarActionPerformed
        setCamposEnabled(false);
        this.iniciar.setEnabled(false);
        this.cancelar.setEnabled(true);

        Email email = new Email();
        email.setRemitente(this.remitente.getText());
        email.setDestinatario(this.destinatario.getText());
        email.setSubject(this.asunto.getText());
        email.setBody(this.mensaje.getText());

        String urlServidor = this.servidor.getText();
        int numPeticiones = (int) this.peticiones.getValue();
        int numHilos = (int) this.hilos.getValue();

        if (this.paralelo.isSelected()) {
            this.bomber = new BomberParalelo(urlServidor, email, numHilos, numPeticiones);
        } else {
            this.bomber = new BomberBatch(urlServidor, email, numHilos, numPeticiones);
        }

        this.bomber.addUpdateListener(new SwingBomberUpdateListener(
                this.porcentaje,
                this.enviado,
                this.error,
                numPeticiones
        ));
        
        this.bomber.addUpdateListener((peticionesHechas, peticionesRechazadas) -> {
            this.apiEstadoCliente.updateEstadoAtaque(peticionesHechas, peticionesRechazadas);
        });

        boolean esAtaqueRemoto = evt == null;

        this.apiEstadoCliente.setEstadoAtacando(numPeticiones, esAtaqueRemoto);
        
        AtaqueRemoto ataqueRemoto = new AtaqueRemoto();
        ataqueRemoto.setNumHilos(numHilos);
        ataqueRemoto.setNumIteraciones(numPeticiones);
        ataqueRemoto.setUrlServidorAtacado(urlServidor);
        ataqueRemoto.setEnParalelo(this.paralelo.isSelected());
        this.servidoresRemotos1.enviarAtaque(ataqueRemoto);

        this.bomber.start();

        new Thread(() -> {
            while (!this.bomber.ataqueTerminado()) {
                try {
                    Thread.sleep(500);
                } catch (InterruptedException ex) {
                    ex.printStackTrace();
                }
            }
            setCamposEnabled(true);
            this.iniciar.setEnabled(true);
            this.cancelar.setEnabled(false);
            this.jLabelConexion.setEstado(JLabelEstadoConexion.estado.ESCUCHANDO);
            this.apiEstadoCliente.setEstadoAtaqueTerminado();
        }).start();

    }//GEN-LAST:event_iniciarActionPerformed

