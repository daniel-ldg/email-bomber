    private void setupAtaqueRemotoDaemon() {
        this.ataqueRemotoDaemon = new AtaqueRemotoDaemon(
                new AtaqueRemotoHandler() {
            @Override
            public void handleNuevoAtaque(AtaqueRemoto ataqueRemoto) {
                jLabelConexion.setEstado(JLabelEstadoConexion.estado.ATACANDO);
                if (bomber != null && !bomber.ataqueTerminado()) {
                    cancelarActionPerformed(null);
                    JOptionPane.showMessageDialog(
                            null,
                            "Ataque remoto recibido. Se debe cancelar ataque actual.",
                            "Mensaje",
                            JOptionPane.INFORMATION_MESSAGE
                    );
                }
                servidor.setText(ataqueRemoto.getUrlServidorAtacado());
                hilos.setValue(ataqueRemoto.getNumHilos());
                peticiones.setValue(ataqueRemoto.getNumIteraciones());
                if (ataqueRemoto.isEnParalelo()) {
                    paralelo.setSelected(true);
                } else {
                    batch.setSelected(true);
                }
                iniciarActionPerformed(null);
                cancelar.setEnabled(false);
            }

            @Override
            public void conexionEstablecida() {
                jLabelConexion.setEstado(JLabelEstadoConexion.estado.ESCUCHANDO);
            }

            @Override
            public void conexionPerdida() {
                jLabelConexion.setEstado(JLabelEstadoConexion.estado.NO_CONECTADO);
            }

            @Override
            public void abortarAtaqueRemoto() {
                cancelarActionPerformed(null);
            }
        },
                "http://localhost:5000/api/datosataque"
        );
    }

